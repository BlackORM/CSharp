void ImagePrint(int[,] ris)
{
    for (int i = 0; i < ris.GetLength(0); i++)
    {
        for (int j = 0; j < ris.GetLength(1); j++)
        {
            if (ris[i, j] == 0) Console.Write($" ");
            else if (ris[i, j] == 1) Console.Write($"█");
            else Console.Write($"░");

        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 2;
        FillImage(row-1,col);
        FillImage(row,col-1);
        FillImage(row+1,col);
        FillImage(row,col+1);
    }

}
